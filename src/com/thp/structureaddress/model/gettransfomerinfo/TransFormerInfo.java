package com.thp.structureaddress.model.gettransfomerinfo;
/**
 * 
 * @author dingjinyang
 * 变压器信息
 */
public class TransFormerInfo {

	private String geometry;
	private TransformerBasicInfo basicInfo;
	private TransformerInfoFromMarketDpt infoFromMarketDpt;
	private TransformerInfoFromPlanDpt InfoFromPlanDpt;
	private TransformerFrequencyCutInfo frequencyCutInfo;
	
	public void setGeometry(String geometry) {
		this.geometry = geometry;
	}
	public String getGeometry() {
		return geometry;
	}
	public TransformerBasicInfo getBasicInfo() {
		return basicInfo;
	}
	public void setBasicInfo(TransformerBasicInfo basicInfo) {
		this.basicInfo = basicInfo;
	}
	public TransformerInfoFromMarketDpt getInfoFromMarketDpt() {
		return infoFromMarketDpt;
	}
	public void setInfoFromMarketDpt(TransformerInfoFromMarketDpt infoFromMarketDpt) {
		this.infoFromMarketDpt = infoFromMarketDpt;
	}
	public TransformerInfoFromPlanDpt getInfoFromPlanDpt() {
		return InfoFromPlanDpt;
	}
	public void setInfoFromPlanDpt(TransformerInfoFromPlanDpt infoFromPlanDpt) {
		InfoFromPlanDpt = infoFromPlanDpt;
	}
	public TransformerFrequencyCutInfo getFrequencyCutInfo() {
		return frequencyCutInfo;
	}
	public void setFrequencyCutInfo(TransformerFrequencyCutInfo frequencyCutInfo) {
		this.frequencyCutInfo = frequencyCutInfo;
	}
	
}
